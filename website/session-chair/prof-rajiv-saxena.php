                                        <p><img class="alignleft" alt="RKS" src="../2013/05/RajivSaxena.jpg" /></p>
<p style="text-align: justify;">Rajiv Saxena obtained B.E. in Electronics &#038; Telecommunication Engineering and M.E. in Digital Techniques &#038; Data Processing. He joined IIT, Roorkee (erstwhile UOR, Roorkee), as a QIP Fellow, towards his Doctoral Degree Program. The Ph. D. degree was conferred on him in Electronics &#038; Computer Engineering. During the research tenure, Prof. Saxena suggested a few corrections in one benchmark paper of Dr. A. Papoulis (a veteran in the field of Communication Engineering) published in the year 1973. These corrections were appreciated and accepted by Dr. A. Papoulis himself.<br />
Prof. Saxena served at Reliance, GRASIM, CIMMCO and Orimpex Textile Industries and had industrial experience of automation in textile machinery. He became Lecturer in Electronics Engineering Department of Madhav Institute of Technology and Science, Gwalior. Prof. Saxena became Reader in the same department. Prof. Saxena was promoted to Professor in Electronics Engineering in April 1997 and also served as Head of the Department and Chairman, BOS, at MITS, Gwalior. Prof. Saxena served, on lien from MITS, at Thapar Institute of Engineering &#038; Technology (Now Thapar University), Patiala, as Professor in Electronics &#038; Communication Engineering from June, 2000 to June 2002. He was the founder Head of the Department of Electronics &#038; Communication Engineering at TIET, Patiala. He also served as Principal at Rustam Ji Institute of Technology, BSF Academy, Tekanpur, (again on lien from MITS Gwalior) for a period of two years from January, 2004 to January, 2006.</p>
<p>Prof. Saxena also served as guest faculty at leading institutions including ABV-IIITM, Gwalior, IITTM, Gwalior and in sequential M. Tech. Program of U. P. Technical University, Lucknow. Prof. Saxena delivered various invited talks / keynote addresses at various platforms of repute. Prof. Saxena was involved in counselling / admissions at state and local level. Prof. Saxena served as Co-ordinator of Project IMPACT for a period of two years (project was a joint venture of World Bank and Ministry of Communication &#038; I.T.). Prof. Saxena executed two major research projects funded by AICTE besides the project under FIST program of DST.</p>
<p>Prof. Saxena has supervised THIRTEEN Ph.D. candidates in the area of wireless, cellular, mobile &#038; digital communication, digital signal processing, digital image processing and application of DSP tools in electronic systems. Prof. Saxena has published about 60 research articles in refereed journals of national and international repute (viz. IEEE Trans. on Information Theory, IEEE Trans. on Signal Processing, Signal Processing (Elsevier), Digital Signal Processing (Elsevier), Wireless Personal Communication (Springer), Signal image and Video Processing (Springer), International Journal of Image and Data Fusion (Taylor and Francis), International Journal of Signal and Image Processing, Indian Journal of Telecommunications, Journal of IISc, Bangalore, IETE Journal of Research, IETE Technical Review, IETE Journal of Education, Bentham Science Journals, IE(I) Journal of E&#038;C Division and other popular open access journals etc). Best paper award of IETE was also conferred on him in the year 2008. Prof. Saxena has also supervised about TWENTY FIVE PG Dissertations and many UG Projects in the areas of his interests.</p>
<p>Prof. Saxena had organized various national level seminars/ symposiums/ conferences and workshops. He had conducted several Faculty Development Programs (FDP), Staff Development Programs (SDP) and QIP Short Term Training Programs (STTP) sponsored by ISTE and AICTE jointly, and also delivered lectures in these programs at various places. Prof. Saxena is a Fellow of IETE and Sr. Member of CSI and ISTE. </p>
