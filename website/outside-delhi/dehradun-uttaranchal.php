<p>&nbsp;</p>
<p>Distance (from Delhi) &#8211; 245km (4h 20m)</p>
<p align="justify">One among the oldest cities of India, Dehradun houses all that is elegant and bright in the nature. A historic town in the lap of the majestic Himalayas, this pretty town is now the capital of the newly formed State of Uttaranchal. A sprawling city, which is speckled with picturesque places, allures scores of picnickers. These places encompass rambling gardens, parks, well-regarded temples and verdant landscapes.</p>
<p align="justify">An important province under the former Garhwal rulers, this hilly terrain is also renowned as the abode of Drona, the mighty guru of the Kauravas. The city boasts of numerous legends about it and most of them are related to the Great Epic Mahabharatha.</p>
<p align="justify">Dehradun presents a city with excellent credentials in the field of knowledge and information. Its renowned academic institutions received worldwide accolades and some of them are in exist from the time of the British Rule. This classy city with an array of good quality boarding schools, the town presents a bustling appearance.</p>
<p align="justify">This pictorial place is an ideal hotspot for those who really want to take risks in their life. A wide range of escapades is in offer in this hill station. With enormous places for paragliding, skiing, angling and river rafting, Dehradun fascinates the adventure buffs.</p>
