								<p><img class="size-full wp-image-233 alignleft" alt="" src="../2013/03/Screenshot-22.png"></p>
<p style="text-align: justify;">Prof. Karmeshu is Professor in School of Computer &amp; Systems Sciences, at Jawaharlal Nehru University (JNU) New Delhi India, since 1986. Prior to joining JNU, New Delhi, he taught in Department of Systems Engineering at the University of Waterloo, Canada and University of Mexico (UAM) and UNAM, Mexico. Prof. Karmeshu had his education in Physics from University of Delhi, where he scored highest percentage of marks in all Honour’s courses and was awarded Gold Medal. After finishing his M.Sc., he completed his PhD from the same University in the area of Stochastic Modeling of diffusion of Brownian particles.</p>
<p style="text-align: justify;">Thereafter in 1976 he proceeded to University of Stuttgart to work with Prof. Haken, founder of Synergetics. He broadened his research interests and started working in the area of stochastic modeling of non-linear problems arising in variety of systems ranging from physical, engineering, to Socio-technical systems. His earlier work on noise in nuclear reactors and diffusion of innovation are well cited and has been included in several books/monographs. He has published over hundred research papers in refereed journals and has edited one book on “Entropy measures, maximum entropy principle and emerging applications” from (Springer- Verlag) and two special issues of JSIR (CSIR Golden jubilee year) on Mathematical modeling of Innovation Diffusion and R&amp;D Management. He has delivered several invited seminars/lectures in India and other countries. He serves on a number of expert committees. Prof. Karmeshu is recipient of Shanti Swarup Bhatnagar Award in Mathematical Sciences (1993) and C.M. Jacob Gold Medal, System Society of India (1990).</p>
